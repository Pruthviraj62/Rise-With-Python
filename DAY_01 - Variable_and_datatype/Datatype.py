# 1. int — whole numbers (no decimal)
score = 100
year = 2024
temperature = -5

# 2. float — numbers with decimal point
pi = 3.14159
price = 99.99
percentage = 87.5

# 3. str — text, always in quotes
name = "Priya"
city = 'Mumbai'          # single or double quotes, both work
greeting = "Namaste!"

# 4. bool — only two values: True or False (capital T and F)
is_raining = False
has_passed = True


print(5 + 3)        # 8       (int + int = int)
print(5 + 3.0)      # 8.0     (int + float = float)
print("5" + "3")    # "53"    (str + str = joins them!)
#print(5 + "3")      # ERROR!  (can't mix int and str)